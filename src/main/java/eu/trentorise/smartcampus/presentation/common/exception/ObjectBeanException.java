package eu.trentorise.smartcampus.presentation.common.exception;

public class ObjectBeanException extends Exception {

	private static final long serialVersionUID = 8192286081992662655L;

	public ObjectBeanException() {
		super();
	}

	public ObjectBeanException(String message, Throwable cause) {
		super(message, cause);
	}

	public ObjectBeanException(String message) {
		super(message);
	}

	public ObjectBeanException(Throwable cause) {
		super(cause);
	}

}
